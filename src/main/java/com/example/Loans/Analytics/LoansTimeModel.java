package com.example.Loans.Analytics;

public class LoansTimeModel {

	private int Year ; 
	private int Month ;
	
	public LoansTimeModel() {}
	
	public LoansTimeModel(int year, int month) {
		super();
		Year = year;
		Month = month;
	}
	
	public int getYear() {
		return Year;
	}
	public void setYear(int year) {
		Year = year;
	}
	public int getMonth() {
		return Month;
	}
	public void setMonth(int month) {
		Month = month;
	} 
	
	
	
	
}
