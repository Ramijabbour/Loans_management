package com.example.settelmets.Analytics;

public class TimeModel {

	private int Month ; 
	private int Year ;
	private int Day ; 
	public int getMonth() {
		return Month;
	}
	public void setMonth(int month) {
		Month = month;
	}
	public int getYear() {
		return Year;
	}
	public void setYear(int year) {
		Year = year;
	}
	public int getDay() {
		return Day;
	}
	public void setDay(int day) {
		Day = day;
	} 

}
